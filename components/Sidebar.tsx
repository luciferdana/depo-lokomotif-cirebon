export default function Sidebar({ user, dashboardTitle }: SidebarProps) {
    const pathname = usePathname()
    const router = useRouter()
    const roleType = user.roleType

    const handleLogout = () => {
        clearSession()
        router.push('/')
    }

    const basePath = `/${roleType}`

    // Menu items based on role
    const menuItems = roleType === 'mekanik'
        ? [
            { href: `${basePath}/dashboard`, label: 'Dashboard', icon: DashboardIcon },
            { href: `${basePath}/checksheet`, label: 'Checksheet', icon: ChecksheetIcon },
            { href: `${basePath}/equipment`, label: 'Equipment', icon: EquipmentIcon },
        ]
        : [
            { href: `${basePath}/dashboard`, label: 'Dashboard', icon: DashboardIcon },
        ]

    return (
        <aside className="sidebar">
            <div className="sidebar-logo">
                <img src="/logo.jpeg" alt="KAI Logo" style={{ width: 40, height: 40, borderRadius: 8, objectFit: 'cover' }} />
                <span className="sidebar-logo-text">{dashboardTitle}</span>
            </div>

            <div className="sidebar-section">
                <p className="sidebar-section-title">Menu Utama</p>
                <nav className="sidebar-menu">
                    {menuItems.map((item) => (
                        <Link
                            key={item.href}
                            href={item.href}
                            className={`sidebar-link ${pathname === item.href ? 'active' : ''}`}
                        >
                            <item.icon />
                            {item.label}
                        </Link>
                    ))}
                </nav>
            </div>

            <div className="sidebar-bottom">
                <div className="sidebar-section">
                    <p className="sidebar-section-title">Pengaturan</p>
                    <nav className="sidebar-menu">
                        <Link
                            href={`${basePath}/settings`}
                            className={`sidebar-link ${pathname === `${basePath}/settings` ? 'active' : ''}`}
                        >
                            <SettingsIcon />
                            Settings
                        </Link>
                        <button onClick={handleLogout} className="sidebar-link" style={{ background: 'none', border: 'none', width: '100%', textAlign: 'left', cursor: 'pointer' }}>
                            <LogoutIcon />
                            Logout
                        </button>
                    </nav>
                </div>
            </div>
        </aside>
    )
}
